const normalizePhrasalMeta = (meta = {}) => {
  const sourceRefs = Array.isArray(meta.sourceRefs)
    ? meta.sourceRefs.filter((source) => source && (source.label || source.shortLabel || source.url))
    : []

  const sourceBadges = [
    ...(Array.isArray(meta.sourceBadges) ? meta.sourceBadges : []),
    ...sourceRefs.map((source) => source.shortLabel || source.label).filter(Boolean),
  ]

  return {
    sourceRefs,
    sourceBadges: [...new Set(sourceBadges.map((value) => String(value || '').trim()).filter(Boolean))],
  }
}

